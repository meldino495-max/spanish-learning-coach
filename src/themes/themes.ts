export const DEFAULT_THEME: ThemeId = 'midnight';

export const THEME_MAP = Object.fromEntries(THEMES.map((t) => [t.id, t])) as Record<
  ThemeId,
  ThemeDefinition
>;

export function applyTheme(id: ThemeId) {
  const theme = THEME_MAP[id] ?? THEME_MAP.midnight;
  const root = document.documentElement;
  root.setAttribute('data-theme', id);
  root.setAttribute('data-tech', theme.group === 'tech' ? 'true' : 'false');
  root.setAttribute('data-comic', theme.group === 'comic' ? 'true' : 'false');
  root.setAttribute('data-chiikawa', theme.group === 'chiikawa' ? 'true' : 'false');
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value);
  }
}

